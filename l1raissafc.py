def termo_geral_pg(primeiro_termo: float, razao: float, n: int) -> float:
    """
    Calcula o n-ésimo termo de uma Progressão Geométrica.

    Args:
        primeiro_termo (float): O primeiro termo da PG (a1).
        razao (float): A razão da PG (q).
        n (int): A posição do termo a ser encontrado.

    Returns:
        float: O valor do n-ésimo termo.
    """
    # Validação para garantir que n seja um inteiro positivo.
    if not isinstance(n, int) or n <= 0:
        raise ValueError("O valor de 'n' deve ser um inteiro positivo.")

    # A fórmula a_n = a1 * q**(n-1) implementada diretamente com operadores Python.
    termo_n = primeiro_termo * (razao ** (n - 1))
    return termo_n

def soma_pg(primeiro_termo: float, razao: float, n_termos: int) -> float:
    """
    Calcula a soma dos n primeiros termos de uma Progressão Geométrica.

    Args:
        primeiro_termo (float): O primeiro termo da PG (a1).
        razao (float): A razão da PG (q).
        n_termos (int): O número de termos a serem somados.

    Returns:
        float: A soma dos n primeiros termos.
    """
    if not isinstance(n_termos, int) or n_termos <= 0:
        raise ValueError("O número de termos 'n' deve ser um inteiro positivo.")

    # Caso especial: se a razão for 1, a PG é uma constante.
    if razao == 1:
        return primeiro_termo * n_termos

    # A fórmula Sn = a1 * (q**n - 1) / (q - 1) implementada diretamente.
    soma_dos_termos = primeiro_termo * (razao ** n_termos - 1) / (razao - 1)
    return soma_dos_termos

